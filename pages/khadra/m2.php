  <p class="MsoNormal" dir="rtl"><span lang="AR-EG">كان من الطبيعي أن يتطلع عمر بن العاص على فتح افريقية بعد أن تم فتح مصر للأسباب: </span></p>

  <p class="MsoNormal" dir="rtl"><span lang="AR-EG">1</span><span lang="AR-EG">– تامين فتح مصر من خطر المستوطنات البيزنطية المقيمة على السواحل المغربية. </span></p>

  <p class="MsoNormal" dir="rtl"><span lang="AR-EG">2- نشر الدين ورفع راية الجهاد لمواصله نشر الدين </span></p>

  <p class="MsoNormal" dir="rtl"><span lang="AR-EG">3- القضاء على كل من يخالف شرع الله </span></p>

